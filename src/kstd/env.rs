use std::time::{SystemTime, UNIX_EPOCH};

pub fn now_micros() -> u64 {
    let now = SystemTime::now();
    let duration_since_epoch = now.duration_since(UNIX_EPOCH).expect("Time went backwards");
    duration_since_epoch.as_micros() as u64
}
